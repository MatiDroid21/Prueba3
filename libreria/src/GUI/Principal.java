    void cargarDatos(){
         int sw = 0;
        ArrayList<Cliente> clientes = icliente.clientes();
        for (int i = 0; i < clientes.size(); i++) {
            if (sw == 0) {
                cboCliente.addItem("Seleccione");
                sw = 1;
            }
            cboCliente.addItem(clientes.get(i).getNombre());

        }
        cboCliente.setSelectedIndex(0);

        int sw2 = 0;
        ArrayList<Libro> libros = ilibro.libros();
        for (int i = 0; i < libros.size(); i++) {
            if (sw2 == 0) {
                cboLibros.addItem("Seleccione");
                sw = 2;
            }
            cboLibros.addItem(libros.get(i).getTitulo());
        }
        cboLibros.setSelectedIndex(0);
    }
    
